UIFty = {Name = "UIFty"}

local My = UIFty

--模块字典
My.dic = {}

--添加模块
--path(string):模块路径
function My.Add(path)
	local mod = require(path)
	if mod == nil then
		iTrace.Error("Loong", "UI模块:", path, "未返回")
		return
	end
	local name = mod.Name
	if name == nil then
		iTrace.Error("Loong", "UI模块:", path, "无Name字段")
		return
	end
	local dic = My.dic
	local it = dic[name]
	if it then
		iTrace.Error("Loong", "重复添加UI模块:", path)
	else
		dic[name] = mod
	end
end

