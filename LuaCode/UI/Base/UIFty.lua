--根据UI类型名称创建 指定UI
function My.Create(name)
	local ui = My.dic[name]
	if ui == nil then
		iTrace.Error("Loong", "未在UIFty中添加:" .. name)
	end
	return ui
end

--判断是否包含UI
--name(string):ui名称
function My.Contains(name)
	if name == nil then return false end
	local ui = My.dic[name]
	if ui then
		return true
	else
		return false
	end
end

--获取模块
function My.Get(name)
	local ui = My.dic[name]
	do return ui end
end

return My
